def sumar(op1,op2):
    print(f'El resultado de la suma es: {op1+op2}')

def restar(op1,op2):
    print(f"El resultado de la resta es: {op1-op2}")

def multiplica(op1,op2):
    print(f"El resultado de la multiplicación es: {op1*op2}")

def dividir(op1,op2):
    print(f"El resultado de la resta es: {op1/op2}")

def potencia(op1,op2):
    print(f"El resultado de la potenciación es: {op1**op2}")